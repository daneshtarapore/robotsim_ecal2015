#include "aggregatebehavior.h"

/******************************************************************************/
/******************************************************************************/

CAggregateBehavior::CAggregateBehavior() 
{
}

/******************************************************************************/
/******************************************************************************/
    
bool CAggregateBehavior::TakeControl() 
{
    return true;
}

/******************************************************************************/
/******************************************************************************/

void CAggregateBehavior::Action()
{
    TPosition tCenterOfMass = m_pcAgent->GetCenterOfMassOfSurroundingAgents(10, ANY);
    if (tCenterOfMass.m_fX != 0.0 && tCenterOfMass.m_fY != 0.0) 
    {
        m_pcAgent->MoveTowards(tCenterOfMass, m_pcAgent->GetMaximumSpeed());
    }
}

